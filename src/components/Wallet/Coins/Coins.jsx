const itemsPerPage = 10;

const Coins = () => {
  const [searchState, setSearchState] = useState("");
  const [currentItems, setCurrentItems] = useState([]);
  const [data, setData] = useState(dataRaw);
  const [itemOffset, setItemOffset] = useState(0);
  const [pageCount, setPageCount] = useState(0);

  const handlePageClick = (event) => {
    const newOffset = (event.selected * itemsPerPage) % data.length;
    // console.log(
    //   `User requested page number ${event.selected}, which is offset ${newOffset}`
    // );
    setItemOffset(newOffset);
  };

  useEffect(() => {
    const endOffset = itemOffset + itemsPerPage;
    setCurrentItems(data.slice(itemOffset, endOffset));
    setPageCount(Math.ceil(data.length / itemsPerPage));
  }, [data, itemOffset]);

  useEffect(() => {
    const filtered = dataRaw.filter((el) =>
      el.coin.toLowerCase().includes(searchState.toLocaleLowerCase())
    );
    setData(filtered);
  }, [searchState]);

  return (
    <div className={classes.coins}>
      <div className={classes.filterContainer}>
        <div className={classes.searchContainer}>
          <img src={search} alt="search" />
          <input
            type="text"
            value={searchState}
            onChange={(e) => setSearchState(e.target.value)}
            placeholder="Search Coin"
          />
        </div>
        <Checkbox label="Hide Small Balances" />
      </div>
      <div className={classes.dataTable}>
        <div className="table-container">
          <div className={classes.table}>
            <DataTable columns={columns} data={currentItems} />{" "}
          </div>
        </div>
        <ReactPaginate
          className="coins-pagination"
          breakLabel="..."
          nextLabel={<img src={pageArrowRight} alt="arrow" />}
          onPageChange={handlePageClick}
          pageRangeDisplayed={2}
          pageCount={pageCount}
          previousLabel={<img src={pageArrowLeft} alt="arrow" />}
          renderOnZeroPageCount={null}
          marginPagesDisplayed={2}
        />
      </div>
    </div>
  );
};

export default Coins;
